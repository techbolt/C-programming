#include <stdio.h>

int main()
{
    printf("Hello \" World\n"); //if we put a forward slash(\) before a quotation mark, it will print tthe quotation mark too

    printf("My favourite %s is %d\n", "number", 1000);

    printf("%d", 5+6);

    return 0;
}

